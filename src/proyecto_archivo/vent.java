    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        nodo aux = read.devolver().getinicio();
        String art = artista.getText();
        while (aux != null) {

            if (art == aux.getartista()) {

                aux.setpath(txtpath.getText());
                aux.setdisquera(disquera.getText());
                aux.setalbum(album.getText());
                aux.setartista(artista.getText());
                aux.setaño(año.getText());
                aux.setgenero(genero.getText());
                aux.setpagD(pag_dis.getText());
                aux.setPagA(pag_art.getText());
                aux.settrack(track.getText());

                System.out.println("el nuevo genero es" + aux.getgenero());
                aux = aux.getSiguiente();

            } else {

                aux = aux.getSiguiente();
            }

        }

        aux = read.devolver().getinicio();
    }//GEN-LAST:event_editActionPerformed

