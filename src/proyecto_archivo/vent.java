    public void rut() {

        JFileChooser fc = new JFileChooser();

        fc.setMultiSelectionEnabled(true);
        fc.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);

        byte d = (byte) fc.showOpenDialog(null);
        if (d == 0) {
            String ruta = fc.getSelectedFile().toString();
            paths(new File(ruta));

        }
    }

    public void paths(File ruta) {
        for (final File ficheroEntrada : ruta.listFiles()) {
            if (ficheroEntrada.isDirectory()) {
                paths(ficheroEntrada);
            } else {
                if (ficheroEntrada.getName().endsWith("mp3")) {

                    System.out.println("ruta->> " + ficheroEntrada.getAbsolutePath());
                    System.out.println("NOMBRE: " + ficheroEntrada.getName() + " RUTA DEL ARCHIVO: " + ficheroEntrada.getAbsolutePath());
                    read.lectura(ficheroEntrada.getAbsolutePath());

                }

            }
        }
    }

