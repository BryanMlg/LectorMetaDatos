    private void cargarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cargarMousePressed
        setColor(cargar);
        rut();
        ResetColor(cargar);
        read.mostrar();

        //pone los indices
        //indice por nombre
        if (!read.devolver().esVacia()) {

            nodo aux = read.devolver().getinicio();
            while (aux != null) {

                combo.addItem(aux.getcancion());

                aux = aux.getSiguiente();

            }

        }

        //Indice por artista
        if (!read.devolver().esVacia()) {

            nodo aux = read.devolver().getinicio();
            while (aux != null) {

                comboartista.addItem(aux.getartista());

                aux = aux.getSiguiente();

            }

        }

        //Indice por album
        if (!read.devolver().esVacia()) {

            nodo aux = read.devolver().getinicio();
            while (aux != null) {

                comboalbum.addItem(aux.getalbum());

                aux = aux.getSiguiente();

            }

        }
        JOptionPane.showMessageDialog(null, "Se ha cargado correctamente");
        dis.setVisible(true);
        disquera.setVisible(true);
        art.setVisible(true);
        artista.setVisible(true);
        alb.setVisible(true);
        album.setVisible(true);
        anio.setVisible(true);
        año.setVisible(true);
        gen.setVisible(true);
        genero.setVisible(true);
        path.setVisible(true);
        txtpath.setVisible(true);
        pgart.setVisible(true);
        pag_art.setVisible(true);
        pgdis.setVisible(true);
        pag_dis.setVisible(true);
        nopis.setVisible(true);
        track.setVisible(true);
        nombre.setVisible(true);
        name.setVisible(true);

    }//GEN-LAST:event_cargarMousePressed

    private void comboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboActionPerformed

        nodo aux = read.devolver().getinicio();
        while (aux != null) {

            if (combo.getSelectedItem() == aux.getcancion()) {

                txtpath.setText(aux.getpath());
                disquera.setText(aux.getdisquera());
                album.setText(aux.getalbum());
                artista.setText(aux.getartista());
                año.setText(aux.getaño());
                genero.setText(aux.getgenero());
                pag_dis.setText(aux.getpagD());
                pag_art.setText(aux.getPagA());
                track.setText(aux.gettrack());
                name.setText(aux.getcancion());
                aux = aux.getSiguiente();

            } else {

                aux = aux.getSiguiente();
            }

        }

        aux = read.devolver().getinicio();


    }//GEN-LAST:event_comboActionPerformed

